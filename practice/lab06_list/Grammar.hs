  = AtomInt Int
  | AtomId String
  | AtomSym Char
  | AtomNil
  deriving (Eq, Show)

getDepth :: Sexp -> Int 
getDepth (Atom _ i) = i 
getDepth (Pair _ _ i) = i 

increaseDepth :: Sexp -> Sexp 
